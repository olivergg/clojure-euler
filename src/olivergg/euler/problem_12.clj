(defn tn
  "nth triangular number"
  [n]
  (/ (* n (- n 1)) 2)
  )

(time (first (for [n (range 10000 15000)
                   :let [tofn (tn n)
                         c (countdiv tofn)
                         ]
                   :when (> c 500)
                   ]
               (do
                 (println tofn c n)
                 tofn
                 )
               )
             )
      )
; 76576500 (the 12376th triangular number)
